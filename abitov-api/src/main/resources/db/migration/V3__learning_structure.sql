create table if not exists programs (
    id bigint generated by default as identity primary key,
    course_id bigint not null,
    title varchar(200) not null,
    description varchar(2000),
    position integer not null,
    active boolean not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_program_course foreign key (course_id) references courses(id)
);

create table if not exists lessons (
    id bigint generated by default as identity primary key,
    program_id bigint not null,
    title varchar(200) not null,
    body varchar(4000),
    position integer not null,
    active boolean not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_lesson_program foreign key (program_id) references programs(id)
);

create table if not exists training_tests (
    id bigint generated by default as identity primary key,
    program_id bigint not null,
    lesson_id bigint not null unique,
    title varchar(200) not null,
    pass_score integer not null,
    max_attempts integer not null,
    active boolean not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_test_program foreign key (program_id) references programs(id),
    constraint fk_test_lesson foreign key (lesson_id) references lessons(id)
);

create table if not exists test_questions (
    id bigint generated by default as identity primary key,
    test_id bigint not null,
    question_text varchar(2000) not null,
    position integer not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_question_test foreign key (test_id) references training_tests(id)
);

create table if not exists test_options (
    id bigint generated by default as identity primary key,
    question_id bigint not null,
    option_text varchar(1000) not null,
    is_correct boolean not null,
    position integer not null,
    constraint fk_option_question foreign key (question_id) references test_questions(id)
);

create table if not exists test_attempts (
    id bigint generated by default as identity primary key,
    test_id bigint not null,
    client_id bigint not null,
    attempt_no integer not null,
    score integer not null,
    passed boolean not null,
    taken_at timestamp not null,
    answers_json varchar(4000),
    constraint fk_attempt_test foreign key (test_id) references training_tests(id),
    constraint fk_attempt_client foreign key (client_id) references app_users(id)
);

